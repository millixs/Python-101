# We will cover some basic and most used operators in Python

friends = 14

# friends = friends + 1        # Addition Operator (Increases the value by 1)
# friends += 1                 # Augmented Addition Operator (Shorter way to add 1)

# friends = friends - 1        # Subtraction Operator (Decreases the value by 1)
# friends -= 1                 # Augmented Subtraction Operator (Shorter way to subtract 1)

# friends = friends * 3        # Multiplication Operator (Multiplies the value by 3)
# friends *= 3                 # Augmented Multiplication Operator (Shorter way to multiply by 3)

# friends = friends / 3        # Division Operator (Divides the value by 3, returns float)
# friends /= 3                 # Augmented Division Operator (Shorter way to divide by 3)

# friends = friends % 3        # Modulus Operator (Gives remainder when divided by 3)
# friends %= 3                 # Augmented Modulus Operator (Shorter way to get remainder)

# friends = friends ** 2       # Exponentiation Operator (Raises value to the power of 2)
# friends **= 2                # Augmented Exponentiation Operator (Shorter way to raise to power)

print(friends)
