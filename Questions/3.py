# Program to create a 'Madlibs Game'
# Description: It is a word game where you can create a story by filling in the blanks with random words
# Noun: A word for a person, place, or thing (e.g., dog, school, pencil).
# Adjective: A word that describes a noun (e.g., happy, tall, blue).
# Verb: A word that shows action (e.g., run, jump, eat).

adjective1 = input("Enter an adjective (description): ")
noun1 = input("Enter a noun (person, place, thing): ")
verb1 = input("Enter an action verb (e.g., run, jump, cry): ")
adjective2 = input("Enter an adjective (description): ")
noun2 = input("Enter a noun (person, place, thing): ")

print(f"One {adjective1} morning, I found a {noun1} in my backyard.")
print(f"It suddenly started to {verb1}, which was quite {adjective2}.")
print(f"Curious, I picked up my {noun2} and ran after it.")
print(f"That day turned out to be the most unexpected adventure of my life!")